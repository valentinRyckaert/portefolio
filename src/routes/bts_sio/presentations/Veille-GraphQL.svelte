    <br>
</div>
<div class="carousel-item w-100 text-center">
    <h3>Collecte et organisation des informations</h3>
    <br>
    <ul style="list-style-type: none;">
        <li>Recherche d'un article ou d'une donnée</li>
        <li>Catégorisation de l'information</li>
        <li>Identification des idées clés et des mots-clés</li>
