<div class="carousel-item w-100 text-center">
    <h3>Le GraphQL</h3>
    <br>
    <p>Le GraphQL est un type d'architecture d'API et un langage de requêtage.</p>
    <p>Créé en 2015 par Facebook, il concurrence l'architecture REST.</p>
