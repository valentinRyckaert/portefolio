    <p style="white-space: pre;">

Grandes idées :
    • L'architecture REST est bien plus recherchée sur Google que le GraphQL
    • L'architecture REST connait depuis 6-7 ans un taux de recherche plus chaotique
    • Le GraphQL connait une légère baisse en nombre de recherche

Questions :
    • Y a-t-il des études plus précises mesurant la popularité des deux technologies ?
