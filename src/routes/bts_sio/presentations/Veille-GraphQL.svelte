        <li>Analyse et conclusion</li>
    </ul>
</div>
<div class="carousel-item w-100 text-center">
    <h3>Catégorisation</h3>
    <br>
    <div class="row">
        <div class="col">
            <h5>Catégories :</h5>
            <ul style="list-style-type: none;">
                <li>Article Explicatif</li>
                <li>Etudes</li>
                <li>Articles d'analyse</li>
                <li>Observations</li>
            </ul>
        </div>
        <div class="col">
            <h5>Fiabilité :</h5>
            <ul style="list-style-type: none;">
                <li>Très élevée</li>
                <li>Élevée</li>
                <li>Moyenne</li>
                <li>Basse</li>
            </ul>
        </div>
    </div>
    <br>
    <div class="text-center">
        A noter que les observations ont toujours une fiabilité moyenne ou basse.
    </div>
</div>
<div class="carousel-item w-100 text-center">
    <h3>Organisation des informations</h3>
    <br>
    <div class="row">
        <div class="col">
            <img src="images/obsidian.png" alt="logo obsidian" style="max-width: 150px;">
        </div>
        <div class="col">
            <p>une note Obsidian = un article / une donnée</p>
            <p>six tags : catégorie, source, date, fiabilité, mots-clés, et question globale rattachée.</p>
        </div>
    </div>
    <br>
    <p>Chaque note est consitituée de trois parties : Lien vers l'article ou la donnée, les grandes idées, les questions soulevées</p>
</div>
<div class="carousel-item w-100 text-center">
    <h3>Notes spécifiques</h3>
    <br>
    <p>La note d'analyse : compile toutes les grandes idées extraites des articles et des données observées. Elles sont triées en fonction de leurs tags.
    <p>La note de synthèse : regroupe les différentes conclusions relatives à chaque question globale</p>
</div>
<div class="carousel-item w-100 text-center">
    <h3>Exemple : Comparaison de la popularité du GraphQL et de REST sur Google Trends</h3>
    <br>
    <p>Catégorie : Etude</p>
    <p>Source : Google</p>
    <p>Date : 3 novembre 2024</p>
    <p>Fiabilité : très élevée</p>
    <p>Mots-clés : REST, GraphQL</p>
    <p>Question globale rattachée : Quelles sont les prévisions concernant la part de marché de GraphQL dans les prochaines années ?</p>
